## Introduction To Single-Cell RNA-Seq Bioinformatics 

In this workshop we will introduce single-cell RNA sequencing and the bioinformatics methods that are used to analyze the data. 
We will cover common computational algorithms for quality control and clustering as well as give participants hands on experience in analyzing single-cell RNA sequencing data with open source bioinformatics tools.

